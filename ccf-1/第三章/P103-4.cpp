#include<iostream>  
#include<cstdio>  
#include<cstdlib>  
using namespace std;  
int main()
{  
    double i, y;  
    cin>>i;  
    if(i <= 100000){  
        y = 0.1 * i;  
    } else if(i > 100000 && i < 200000){  
        y = 0.1 * 100000 + 0.075 * (i - 100000);  
    } else if(i >= 200000 && i < 400000){  
        y = 0.1 * 100000 + 0.075 * 100000 + 0.05 *(i - 200000);  
    } else if(i >= 400000 && i < 600000){  
        y = 0.1 * 100000 + 0.075 * 100000 + 0.05 * 200000 + 0.03 * (i - 400000);  
    } else if(i >= 600000 && i < 1000000){  
        y = 0.1 * 100000 + 0.075 * 100000 + 0.05 * 200000 + 0.03 * 200000 + 0.015 * (i - 600000);  
    } else if(i >= 1000000){  
        y = 0.1 * 100000 + 0.075 * 100000 + 0.05 * 200000 + 0.03 * 200000 + 0.015 * 400000 + 0.01 * (i - 1000000);  
    }  
    printf("%0.3f\n", y);  
    return 0;  
}  
